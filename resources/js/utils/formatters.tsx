// Action buttons component
export const ActionButtons = ({ 
    actions 
}: { 
    actions: Array<{
        label: string;
        href?: string;
        onClick?: () => void;
        variant?: 'primary' | 'secondary' | 'danger';
        icon?: React.ReactNode;
    }> 
}) => {
    const variants = {
        primary: 'text-blue-600 hover:text-blue-900',
        secondary: 'text-gray-600 hover:text-gray-900',
        danger: 'text-red-600 hover:text-red-900',
    };

    return (
        <div className="flex flex-col space-y-1">
            {actions.map((action, index) => (
                action.href ? (
                    <Link
                        key={index}
                        href={action.href}
                        className={`text-sm font-medium ${variants[action.variant || 'primary']}`}
                    >
                        <div className="flex items-center space-x-1">
                            {action.icon && <span className="w-4 h-4">{action.icon}</span>}
                            <span>{action.label}</span>
                        </div>
                    </Link>
                ) : (
                    <button
                        key={index}
                        onClick={action.onClick}
                        className={`text-sm font-medium text-left ${variants[action.variant || 'primary']}`}
                    >
                        <div className="flex items-center space-x-1">
                            {action.icon && <span className="w-4 h-4">{action.icon}</span>}
                            <span>{action.label}</span>
                        </div>
                    </button>
                )
            ))}
        </div>
    );
};

// Image component with fallback
export const ProductImage = ({
    src,
    alt,
    className = "w-10 h-10 rounded-lg object-cover"
}: {
    src?: string;
    alt: string;
    className?: string;
}) => {
    if (!src) {
        return (
            <div className={`bg-gray-200 flex items-center justify-center ${className}`}>
                <svg className="w-6 h-6 text-gray-400" fill="none" stroke="currentColor" viewBox="0 0 24 24">
                    <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={2} d="M4 16l4.586-4.586a2 2 0 012.828 0L16 16m-2-2l1.586-1.586a2 2 0 012.828 0L20 14m-6-6h.01M6 20h12a2 2 0 002-2V6a2 2 0 00-2-2H6a2 2 0 00-2 2v12a2 2 0 002 2z" />
                </svg>
            </div>
        );
    }

    const imageUrl = src.startsWith('/') ? src : `/storage/${src}`;

    return (
        <div className="relative">
            <img
                src={imageUrl}
                alt={alt}
                className={className}
                onError={(e) => {
                    const target = e.target as HTMLImageElement;
                    const parent = target.parentElement;
                    if (parent) {
                        parent.innerHTML = `
                            <div class="bg-gray-200 flex items-center justify-center ${className}">
                                <svg class="w-6 h-6 text-gray-400" fill="none" stroke="currentColor" viewBox="0 0 24 24">
                                    <path stroke-linecap="round" stroke-linejoin="round" stroke-width="2" d="M4 16l4.586-4.586a2 2 0 012.828 0L16 16m-2-2l1.586-1.586a2 2 0 012.828 0L20 14m-6-6h.01M6 20h12a2 2 0 002-2V6a2 2 0 00-2-2H6a2 2 0 00-2 2v12a2 2 0 002 2z" />
                                </svg>
                            </div>
                        `;
                    }
                }}
                loading="lazy"
            />
        </div>
    );
};

// Truncate text
export const truncateText = (text: string, maxLength: number = 50): string => {
    if (text.length <= maxLength) return text;
    return text.substring(0, maxLength) + '...';
};

