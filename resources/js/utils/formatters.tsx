// Stock status formatter
export const getStockStatus = (stock: number, minStock: number) => {
    if (stock === 0) {
        return { text: 'Out of Stock', variant: 'danger' as const };
    }
    if (stock <= minStock) {
        return { text: 'Low Stock', variant: 'warning' as const };
    }
    return { text: 'In Stock', variant: 'success' as const };
};

